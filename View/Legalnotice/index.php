   <h1>TITRE 1 – DISPOSITION GENERALES</h1>


	<h2>Article 1. Dénomination, définition et durée</h2>
	<ul>
		<li>
		    <h4>Alinéa 1</h4>
			<p>Il est fonde entre les adhérant aux présents statuts une association libre, apolitique et a but non lucratif dénommée « SHARME Canada ».<br/>
			L 'association sue nommée est régie par la loi relative a la liberté d’association et par les présents statuts.</p></li>

		<li>
		    <h4>Alinéa 2.</h4>
			L’association a une durée illimitée
		</li>
	</ul>
	<h2>Article 2. Buts et objectifs</h2>
	Cette association a pour but :
	<ul>
	    <li>La solidarité entre ses membres</li>
	    <li>L’entraide</li>
	    <li>La fraternité entre  les membres et les autres groupes culturels</li>
	    <li>Le maintien d’un lien de fraternité et de solidarité entre tous ses</li> membres.
	    <li>L’inculcation d’un esprit de travail et d’entraide.</li>
	    <li>De leur apprendre a faire réfléchir leur désirs, leurs élans spontané devant les exigences de la en commun et a s’incliner devant une et une seule discipline librement consentie.</li>
	    <li>La création d’un esprit de tolérance affectueuse qui fait supporter sans peine les différences d’humeur</li>
	    <li>L’initiation de ses membres a l’apprentissage  de la responsabilité et de la promotion du gout du dialogue constructif.</li>
	    <li>L’assistance a tous ses membres en cas d’événement heureux  ou malheureux.</li>
	</ul>
	<h2>Article 3.Composition </h2>
	L’association se compose des :
	<ul>
	    <li> Membres actifs : est considère comme tel toute personne physique qui aura été accepte par le groupe et qui se soumet au présent statut et règlement intérieur et a passe sa période de probation</li>
	    <li> Observateur : toute personne non membre désirant intègre le groupe,  parraine par un membre actif,  qui observe et se laisse observer.</li>
	</ul>

	<h2>Article 4. Condition d’admission</h2>
	Pour être membre, il faut :
	<ul>
	    <li>Etre parrain e par un membre actif</li>
	    <li>Avoir été accepte par le groupe</li>
	    <li>Avoir passe une période de probation</li>
	    <li>Démontrer son intérêt a faire partir du groupe</li>
	    <li>  Accepter de se soumettre aux statuts et règlement intérieur</li>
	</ul>

	<h1>TITRE 2-ORGANISATION GENERALE :
	ADMINISTRATION ET FONCTIONNEMENT</h1>

	<h2>Article 5.</h2>
	SHARME Canada est constitue des organismes ci-après :
	<ul>
	    <li>Une assemblée Générale</li>
	    <li>Un bureau exécutif</li>
	    <li>Un  comite</li>
	</ul>

	<h2>Article 6.</h2>
	C’est l’organe suprême de l’association. Elle est constituée de tous les membres tels que définis en article 4 des présents statuts.
	Elle se tient une fois par mois, le 1er samedi du mois. En cas de nécessité la date de la réunion peut être modifie en accord avec tous les membres.
	<h2>Article 7</h2>
	<ul>
	    <li>
	        <h4>Alinéa 1.</h4>
		    Le bureau Exécutif applique les grandes lignes de l’association.
		    Les élections sont prévues par l’Assemblée Générale au a défaut un collège électoral désigné par l’assemblée Générale.
	    </li>
		<li>
		    <h4>Alinéa 2.</h4>
		    Tous les postes à pouvoir au bureau sont électifs. Le mode de scrutin est l’élection a la majorité simple.
		</li>
		<li>
		    <h4>Alinéa 3.</h4>
		    Les candidatures sont déposées le jour de l’élection générale.
		    Tous les candidats doivent être membre actif
		</li>
		<li>
		    <h4>Alinéa 4.</h4>
		    La défaillance d’un membre du bureau est constate par les ¾ des membres de l’AG.
		    L’Assemblée General  procédera a son remplacement par une élection au poste concerne dans un délai d’un mois.
		</li>
	</ul>

	<h2>Article 8. Composition du bureau</h2>
	Le bureau exécutif comprend
	<ul>
	    <li>Un président</li>
	    <li>Un secrétaire</li>
	    <li>Un trésorier</li>
	    <li>Un commissaire au compte</li>
	</ul>

	<h2>Article 9.</h2>
	<ul>
		<li>
		    <h4>Alinéa 1. Le Président</h4>
		    Il représente l’association dans tous les actes de la vie civile et juridique et dirige l’Association conformément aux statuts et règlement intérieur.<br/>
		    Il est assiste dans ses taches par le comite et l’assemblée General
		    Le président est l’ordonnateur de toutes les dépenses.<br/>
		    IL est responsable devant le comite des égarements du bureau et de la politique générale de l’association.<br/>
		    En cas de vacance prolongée (3mois) ou de démission L’AG élu un nouveau président.
		</li>
		<li>
		    <h4>Alinéa 3. Le secrétaire General</h4>
		    Il coordonne les  activités du secrétariat et est également le secrétaire de séance de toutes les réunions.<br/>
		    Il rédige les rapports et on désigne un autre membre qui fait le  compte rendu de la dernière réunion avant le début de chaque assise.<br/>
		    Il assure le suivi administratif des affaires de l’association, la garde des documents ainsi  que l’information des décisions prises au cours d’une réunion.
		</li>
		<li>
		    <h4>Alinéa 4. Le Trésorier</h4>
		    Il est responsable des fonds que lui confie l’Association et est consignataire de tous les documents financiers avec le président ou le commissaire aux comptes.
		    Il présente a¯µu comite la situation des comptes et des dépenses de l’association.
		</li>
		<li>
		    <h4>Alinéa 5. Le commissaire aux comptes.</h4>
		    Il est charge du contrôle de la régularité des rapports financiers et comptables de l’association et fait des rapports trimestriel à l’assemblée Générale.
		</li>
		<li>
		    <h4>Alinéa 6.  Le charge des affaires, sociales, culturelles et sportives.</h4>
		    Il est responsable des activités culturelles et sportives. Il applique la politique sociale et assure la cohésion du groupe lors des événements (deuil, mariage, naissance).
		    Il coordonne les activités et les événements.
		</li>
	</ul>


	<h1>TITRE III- L’Assemblée Générale</h1>

	<h2>Article 10. Attributions.</h2>
	<p>
	   Elle est garante de la politique de l’association.<br/>
	   Elle donne les orientations de l’association<br/>
	   Elle veille à l’application conforme des statuts et recèlements intérieurs.<br/>
	   Elle joue le rôle d’arbitre en cas de litige entre les membres.<br/>
	   Elle organise les commissions de modification des statuts ou de règlement intérieur.<br/>
	   Elle étudie les dossiers de candidature a l’admission.<br/>
	   Elle traite des problèmes dont elle est saisie.<br/>
	   Elle autorise toutes les dépenses exceptionnelles.<br/>
	   L’Assemblée Générale siège tous les premiers samedi du mois.<br/>
	</p>

	<h2>Article 11. Composition</h2>
	L’Assemblée Générale est constituée de tous les membres de la réunion.

	<h2>Article 12. Les élections générales.</h2>
	Tous les membres du bureau sont élus à al majorité simple.

	<h2>Article 13. </h2>
	Tous les membres de l’Assemblée Générale sont tenus d’assister aux réunions et débats concernant la bonne marche de l’association.


	<h1>TITRE IV – FINANCES et AUTRES RESSOURCES</h1>

	<h2>Article 14. </h2>
	<ul>
		<li>
		   <h4>Alinéa 1.</h4>
		   Les ressources de l’association proviennent des cotisations des membres.
		</li>
		<li>
		    <h4>Alinéa 2.</h4>
		    Il sera ouvert dans une institution financière de la place un compte au nom de SHARME Canada avec trois signataires et la sortie des fonds est opérée par deux signataires conjointes.
		</li>
		<li>
		    <h4>Alinéa 3.</h4>
		    Les opérations bancaires sont soumises aux signatures du Président, du trésorier et du commissaire aux comptes.
	    </li>
		<li>
		    <h4>Alinéa 4.</h4>
		    Les dépenses de l’association englobent les frais de fonctionnement ou toute autre dépense prévue dans le règlement intérieur.
		</li>
	</ul>



	<h1>TITRE 4. DISPOSITIONS FINALES</h1>

	<h2>Article 15.</h2>
	Toutes les modifications des présents statuts et règlement intérieur est du ressort de l’Assemblée Générale qui peut être saisie par n’importe quel membre de l’association.

	<h2>Article 15. Dissolution</h2>
	<ul>
		<li>
		    <h4>Alinéa 1.</h4>
		    La dissolution de l’Assemblée Générale ne peut être prononcée que par :
		    <ul>
		        <li>Tous les membres de l’Assemblée Générale réunis en session ordinaire ou extraordinaire.</li>
		        <li>Une décision juridique.</li>
		    </ul>
		</li>
		<li>
		    <h4>Alinéa 2.</h4>
		    En cas de dissolution, l’Assemblée Générale Extraordinaire désigne in ou plusieurs liquidateurs charges d’accomplir les formalités subséquents. En aucun cas les membres de l’association ne pourront se voir attribuer une part quelconque des de l’association. L’actif net subsistant sera attribue obligatoirement a une ou plusieurs associations poursuivant des buts similaires et qui seront nommément désigne par l’Assemblée Générale.
		</li>
	</ul>
	<h2>Article 16.</h2>
	<p>Les cas non prévus dans les présents statuts ou règlement intérieur feront l’objet d’une étude de l’Assemblé Générale.  Le projet d’amendement sera présentée a l’AG pour approbation.<br/></p>

	<p>Fait à Montréal 03 Janvier 2015.<br/></p>


	<p>Le secrétariat général.<br/></p>



