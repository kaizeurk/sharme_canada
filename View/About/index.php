					<h2>Bienvenu sur le site de SHARME CANADA</h2>
					<p>Nous sommes une <strong>Association a but non lucratif</strong> qui a pour vocation d'apporter <em>aux nouveaux arrivants</em>, au quebec un soutien et un accompagnement pour une integration en douceur, effet certaines de nos affectivité permettent entre autres la familliarisation avec un nouvel environnement inconnu, la compréhension de la nouvelle société dans laquelle ils arrivent et veulent adhérer, la pris en charge depuis l'aéroprt suivant chacun des cas et bien d'autres encore ... 
					<p>
					   <strong>SHARME</strong>   
					   est une association légalisée à caractère social  à  but non lucratif  qui joue un rôle effectif et efficace dans la protection des familles camerounaises du canada, la  promotion du bienêtre familial et l'intégration de nouveaux arrivants dans leur nouvelle société. 
					</p>
					<p>
					    Le projet de parrainage des nouveaux arrivants par les membres de SHARME au sein de leurs familles est la meilleure preuve de notre esprit. C'est la meilleure façon pour nous de s'assurer que de nouvelles familles arrivant au Québec  puissent se sentir chez  eux et contribuer à  l'épanouissement  du Québec  dans un temps plus court que d'habitude.
					</p>
					<p>
					    D’initiative dans ce domaine, étant donné que  plusieurs organismes similaires existent, nous prenons en charge les potentielles familles beaucoup plutôt c.à.d. au début de leur processus d'immigration question de leur prodiguer les conseils au sujet de la vie de famille au Canada question de le préparer et les outiller pour une intégration exemplaire. Nos services sont gratuits.
					</p>
					<p>
					    L'association a été créée en vue d 'encadrer les efforts entrepris par les familles  immigrantes du Québec,  de répondre à leurs besoins d'intégration et les accompagner dans leurs  projets de développement social .La promotion des idées ,valeurs et des comportements de volontariat ,de bienfaisance et de solidarité est l 'un des fondements de notre participation à la reconstruction des valeurs morales, spirituel et intellectuels de nation en vue d'aboutir à une famille unie et à une nation utile
					</p>
										
					<h2>Projet de parrainage de nouveaux arrivants</h2>
					<p>
					    Après plusieurs années d'action sociale, l'Association pour la protection de la famille au Canda a décidé d'accorder la priorité aux nouveaux arrivants et ce en mettant en œuvre le projet intitulé :«Le parrainage de la famille immigrante au sein de sa famille ».
					</p>
					<p>
					    SHARME élaboré le projet de parrainage des nouveaux arrivants, lequel projet a trouvé un grand écho parmi les bienfaiteurs .Ce projet vise à venir en aide gratuitement aux nouveaux arrivants qui n'ont plus personne pour les orienter et les aider à mieux et vite s’intégrer en leur prodiguant des conseils nécessaires à leur besoins aussi bien matériels que sociaux.
				    </p>
					 
					 <p>
					    L'association vise à élargir le champ d'action de ce projet en vue de couvrir un maximum de nouveaux arrivants. C'est dans ce but qu’elle apporte du secours à une grande tranche de la société, tant sur le plan moral que social, afin que ces nouveaux immigrants ne se sentent pas rejetés et qu'ils prennent conscience de la solidarité et de la fraternité qui les entoure, tout comme ce parrainage apporte bienfait à celui qui l'accomplit, comme l'affirme le Prophète (PSL): «Veux-tu que ton cœur s'attendrisse et que tu atteignes ton objectif ?Sois bon avec l'orphelin ,caresse sa tête et nourris-le de ta nourriture ,ton cœur s'attendrira et tu atteindras ton objectif . »
					 </p>
					 <p>
					    L'importance du projet de parrainage des nouveaux arrivants réside essentiellement dans le fait que ces derniers arrivent le plus souvent avec des enfants de très jeunes âge ayant besoin de surveillance dans les domaines de l'éducation, la santé, la scolarisation sans quoi ils seraient marqués à vie .Tant que cette surveillance et ce soutient interviennent tôt, tant l'action caritative est bénéfique et facilite l'accès aux autres périodes de la vie.
					 </p>
					
					<h3>COMMENT NOUS OPERONS ?</h3>
					
					<p>
					   Nous recevons des demandes de nouveaux immigrants par l’entremise de notre site internet ou des autres familles ayant bénéficiés de nos services, nous effectuons des visites de terrains et de prospection au sein des associations socioculturelles d’ici et d’ailleurs afin de pouvoir établir un diagnostic sur sa situation effective.
					</p>
					<p>
					   Nous recherchons un bienfaiteur au sein de notre association. Qui est disponible et qui accepte de se charger de l’orientation de l’accueil à l’aéroport et de l’intégration et de le suivre dans son intégration jusqu’à la réussite totale. 
					</p>
					<p>
					   Nous mettons en communication le parrain et la famille nécessiteuse pour une prise en charge et sa mise au  courant sur la situation de l’immigrant et de ses besoins.
					</p>
					<p>
					   Nous ne nous contentons pas d'encourager le parrain à fournir son soutien au nouvel immigrant. Nous veillons à établir un lien affectif entre le parrain, l’immigrant et sa famille. Nous évaluons les services et le niveau de satisfaction du bénéficiaire. Nous nous sommes fixés un objectif <<zéro>> pour zéro échec dans note mission pour l’année 2015.
					</p>
					
					<h3>DOMAINES D'INTERVENTION</h3>
					<ul>
					    <li>
					        fournir un service personnalisé en conseil et orientation familiale  au nouvel immigrant sur les réalités de la vie familiale dans sa future terre d’accueil.
					    </li>
					    <li>
				      	    procurer le maximum de conseils et d’orientation nécessaire à une vie dans la dignité aux nouveaux immigrants dans les premiers jours de leur arrivé au canada;
				      	</li>
				      	<li>
					        contribuer à l'éducation morale et civique des familles immigrantes du Québec par le biais d'activités diverses.
					    </li>
					    <li>
					        procurer aux familles et aux enfants les conseils et les orientations vers les services gouvernementaux dont ils ont besoin pour leur bienêtre sociaux-culturel.
					    </li>
					    <li>
					        leur fournir des vêtements d’hiver pour ceux qui arrivent en hiver et que nous jugeons insignifiants leurs moyens financiers.
					    </li>
					    <li>
					        veiller à leur bonne santé (visites médicales, médicaments, soins) pour les familles et les enfants.
					    </li>
					  </ul>
					
					
					
					<h3>OBJECTIFS VISES PAR LE PROJET</h3>
					<p>
					   L'objectif essentiel du projet est de parrainer les nouveaux immigrants au sein et de les intégrer à leur environnement ainsi que de les protéger contre la déperdition, le vagabondage et de les préparer pour qu'ils soient en mesure de se prendre en charge.
					</p>
					
					<h3>DUREE DU PARRAINAGE</h3>
					<ul>
					    <li>Nous parrainons les nouveaux immigrants jusqu'à ce qu’ils se sentent chez eux.
					    </li>
					    <li>
					        nous nous chargeons des nouveaux immigrants jusqu'à ce qu'ils terminent leurs procédures intégration.
					    </li>
					    <li>
					        nous ne les abandonnons pas après les procédures d’intégrations mais nous effectuons un suivi quotidien pour s’assurer du bienêtre du nouvel immigrant et de sa famille.
					    </li>
					 </ul>
					
					<h3>BUDGET DU PROJET</h3>
					<p>
					   Le montant que nous préconisons par défaut pour le parrainage d'un nouvel immigrant est fonction des conditions de vie de celui-ci et de sa situation au moment de son arrivée au Canada. Le budget nous incombe et nous assumons entièrement les couts qu’engendrent ces services.
					</p>
					
					<h3>DOCUMENT A FOURNIR POUR PROFITER D'UN PARRAINAGE</h3>
